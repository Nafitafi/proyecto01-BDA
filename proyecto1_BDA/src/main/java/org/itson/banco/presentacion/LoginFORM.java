    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        String correo = txtCorreoElectronico.getText();
        String contra = txtContrasenia.getText();

        try {
            IClienteBO clienteBO = new ClienteBO(new ClienteDAO(new ConexionBD()));
            
            // Intentamos entrar
            ClienteDTO cliente = clienteBO.login(correo, contra);
            
            // Si no hubo error, avisamos al controlador
            this.dispose(); // Cerramos login
            controlador.loginExitoso(cliente);

        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error de Login", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error técnico: " + e.getMessage());
        }
    }//GEN-LAST:event_btnContinuarActionPerformed

